    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        
        try{
            
            int cpf = Integer.valueOf(jTextField1.getText());
            String nome = jTextField2.getText();
            String endereco = jTextField3.getText();
            String telefone = jTextField4.getText();


            Paciente paciente = new Paciente(cpf, nome, endereco, telefone);
            PacienteBS pacienteBS = new PacienteBS();
            pacienteBS.editarPaciente(paciente);
            
            jButtonEditar.setEnabled(true);
            jButtonSalvar.setEnabled(false);
            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            
            JOptionPane.showMessageDialog(null,"Paciente Editado!");

        }
       catch (Exception e){
             JOptionPane.showMessageDialog(null, e.getMessage());
       }
    }//GEN-LAST:event_jButtonSalvarActionPerformed


